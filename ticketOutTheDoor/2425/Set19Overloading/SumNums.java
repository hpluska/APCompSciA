public class SumNums {

    public SumNums() {

        System.out.println("Nothing to sum!");

    }

    public SumNums(int a, int b) {

        System.out.println(a + b);

    }

    public SumNums(int a, int b, int c) {

        System.out.println(a + b + c);

    }

    public SumNums(double a, double b) {

        System.out.println(a + b);

    }

}
